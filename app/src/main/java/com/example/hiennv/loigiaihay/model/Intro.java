package com.example.hiennv.loigiaihay.model;

public class Intro {
    private String title;
    private int image;

    public Intro(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public int getImage() {
        return image;
    }
}
